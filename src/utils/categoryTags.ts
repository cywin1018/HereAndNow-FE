/**
 * 카테고리 코드에 따라 태그 옵션을 반환합니다.
 * @param categoryCode 카테고리 그룹 코드 (예: 'CT1', 'FD6', 'CE7')
 * @returns 분위기, 시설, 기타 태그 옵션
 */
export const getCategoryTags = (categoryCode: string | undefined | null): CategoryTags => {
  if (!categoryCode) {
    return defaultTags;
  }

  const tags = categoryTagMap[categoryCode];
  if (!tags) {
    console.warn(`[getCategoryTags] 알 수 없는 카테고리 코드: ${categoryCode}`);
    return defaultTags;
  }

  return tags;
};

